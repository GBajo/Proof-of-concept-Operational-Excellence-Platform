def get_text(key: str, lang: str = "es") -> str:
    """
    Devuelve el texto en el idioma solicitado.
    Fallback: español si lang no existe; clave si la clave no existe.
    """
    entry = TRANSLATIONS.get(key)
    if entry is None:
        return key
    return entry.get(lang) or entry.get("es") or key
